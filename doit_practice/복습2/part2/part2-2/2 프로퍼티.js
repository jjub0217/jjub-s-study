/* 프로퍼티
        - 프로퍼티 키 : 객체에서 값을 담고 있는 정보
        - 프로퍼티 값 : 프로퍼티 키의 값*/
//      - 예시
        const fds17 = {
            name: '강주현',
            gender: 'female',
            age: 33
        }
//          name / gender / age  => 프로퍼티 키
//          '강주현' / 'female' / 33  => 프로퍼티 값                    
            console.log(fds17.name); // 강주현
            console.log(fds17.gender); // female
            console.log(fds17.age); // 33

